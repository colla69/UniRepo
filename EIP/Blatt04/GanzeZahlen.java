public class GanzeZahlen {
	public static void main(String[] args) {
		int a = 0b101;
		System.out.println(a);
		a = a / 2;
		System.out.println(a);
		;;;;;;;;
		System.out.println(a);
		a += 1;
		System.out.println(a);
		a = 2;
		a *= a;
		System.out.println(a == 2);
		System.out.println(a = 2);
		a++;
		System.out.println(a);
		System.out.println(5-2+2);
		System.out.println(5/2*2);
		a = (int) 13.7;
		System.out.println(a + 5.3);
		System.out.println(a);
	}
}

